	public struct Color : IAtom
	{
        private uint mColor;

        public Color(byte R, byte G, byte B, byte A)
        {
            mColor = (uint)((A << 24) | (R << 16) | (G << 8) | (B));
        }

        public Color(byte R, byte G, byte B) : this(R,G,B,255)
        {
        }

        public object Value { get { return mColor; } }
    }
